
namespace FluentCells
{
	public enum DisplayFormat
	{

		/// <summary>
		/// Format String: General
		/// </summary>
		General = 0,

		/// <summary>
		/// Format String: 0
		/// </summary>
		Decimal01 = 1,

		/// <summary>
		/// Format String: 0.00
		/// </summary>
		Decimal02 = 2,

		/// <summary>
		/// Format String: #,##0
		/// </summary>
		Decimal03 = 3,

		/// <summary>
		/// Format String: #,##0.00
		/// </summary>
		Decimal04 = 4,

		/// <summary>
		/// Format String: $#,##0;$-#,##0
		/// </summary>
		Currency05 = 5,

		/// <summary>
		/// Format String: $#,##0;[Red]$-#,##0
		/// </summary>
		Currency06 = 6,

		/// <summary>
		/// Format String: $#,##0.00;$-#,##0.00
		/// </summary>
		Currency07 = 7,

		/// <summary>
		/// Format String: $#,##0.00;[Red]$-#,##0.00
		/// </summary>
		Currency08 = 8,

		/// <summary>
		/// Format String: 0%
		/// </summary>
		Percentage09 = 9,

		/// <summary>
		/// Format String: 0.00%
		/// </summary>
		Percentage10 = 10,

		/// <summary>
		/// Format String: 0.00E+00
		/// </summary>
		Scientific11 = 11,

		/// <summary>
		/// Format String: # ?/?
		/// </summary>
		Fraction12 = 12,

		/// <summary>
		/// Format String: # /
		/// </summary>
		Fraction13 = 13,

		/// <summary>
		/// Format String: m/d/yy
		/// </summary>
		Date14 = 14,

		/// <summary>
		/// Format String: md-mmm-yy
		/// </summary>
		Date15 = 15,

		/// <summary>
		/// Format String: md-mmm-yy
		/// </summary>
		Date16 = 16,

		/// <summary>
		/// Format String: mmm-yy
		/// </summary>
		Date17 = 17,

		/// <summary>
		/// Format String: h:mm AM/PM
		/// </summary>
		Time18 = 18,

		/// <summary>
		/// Format String: h:mm:ss AM/PM
		/// </summary>
		Time19 = 19,

		/// <summary>
		/// Format String: h:mm
		/// </summary>
		Time20 = 20,

		/// <summary>
		/// Format String: h:mm:ss
		/// </summary>
		Time21 = 21,

		/// <summary>
		/// Format String: m/d/yy h:mm
		/// </summary>
		Time22 = 22,

		/// <summary>
		/// Format String: #,##0;-#,##0
		/// </summary>
		Currency37 = 37,

		/// <summary>
		/// Format String: #,##0;[Red]-#,##0
		/// </summary>
		Currency38 = 38,

		/// <summary>
		/// Format String: #,##0.00;-#,##0.00
		/// </summary>
		Currency39 = 39,

		/// <summary>
		/// Format String: #,##0.00;[Red]-#,##0.00
		/// </summary>
		Currency40 = 40,

		/// <summary>
		/// Format String: _ * #,##0_ ;_ * "_ ;_ @_
		/// </summary>
		Accounting41 = 41,

		/// <summary>
		/// Format String: _ $* #,##0_ ;_ $* "_ ;_ @_
		/// </summary>
		Accounting42 = 42,

		/// <summary>
		/// Format String: _ * #,##0.00_ ;_ * "??_ ;_ @_
		/// </summary>
		Accounting43 = 43,

		/// <summary>
		/// Format String: _ $* #,##0.00_ ;_ $* "??_ ;_ @_
		/// </summary>
		Accounting44 = 44,

		/// <summary>
		/// Format String: mm:ss
		/// </summary>
		Time45 = 45,

		/// <summary>
		/// Format String: h :mm:ss
		/// </summary>
		Time46 = 46,

		/// <summary>
		/// Format String: mm:ss.0
		/// </summary>
		Time47 = 47,

		/// <summary>
		/// Format String: ##0.0E+00
		/// </summary>
		Scientific48 = 48,

		/// <summary>
		/// Format String: @
		/// </summary>
		Text = 49
	}
}
